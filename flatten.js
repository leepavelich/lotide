const flatten = arr => {
  let newArr = [];
  for (let elem of arr) {
    if (!Array.isArray(elem)) {
      newArr.push(elem);
    } else {
      for (let subelem of elem) {
        newArr.push(subelem);
      }
    }
  }
  return newArr;
};

assertArraysEqual(flatten([1, 2, [3, 4], 5, [6]]), [1, 2, 3, 4, 5, 6]);